using namespace std;
using ll = long long;
template <class S, S (*op)(S, S), S (*e)(), class F, S (*mapping)(F, S),
          F (*composition)(F, F), F (*id)()>
struct lazy_segtree {
public:
  lazy_segtree() : lazy_segtree(0){};
  explicit lazy_segtree(int n) : lazy_segtree(vector<S>(n, e())) {}
  explicit lazy_segtree(const vector<S> &v) : _n(int(v.size())) {
    log = ceil_pow2(_n), size = 1 << log, d = vector<S>(2 * size, e()),
    lz = vector<F>(size, id());
    for (int i = 0; i < _n; i++)
      d[size + i] = v[i];
    for (int i = size - 1; i >= 1; i--)
      update(i);
  }
  void set(int p, S x) {
    p += size;
    for (int i = log; i >= 1; i--)
      push(p >> i);
    d[p] = x;
    for (int i = 1; i <= log; i++)
      update(p >> i);
  }
  S get(int p) {
    p += size;
    for (int i = log; i >= 1; i--)
      push(p >> i);
    return d[p];
  }
  S prod(int l, int r) {
    if (l == r)
      return e();
    l += size, r += size;
    for (int i = log; i >= 1; i--) {
      if (((l >> i) << i) != l)
        push(l >> i);
      if (((r >> i) << i) != r)
        push((r - 1) >> i);
    }
    S sml = e(), smr = e();
    while (l < r) {
      if (l & 1)
        sml = op(sml, d[l++]);
      if (r & 1)
        smr = op(d[--r], smr);
      l >>= 1, r >>= 1;
    }
    return op(sml, smr);
  }
  void apply(int p, F f) {
    p += size;
    for (int i = log; i >= 1; i--)
      push(p >> i);
    d[p] = mapping(f, d[p]);
    for (int i = 1; i <= log; i++)
      update(p >> i);
  }
  void apply(int l, int r, F f) {
    if (l == r)
      return;
    l += size, r += size;
    for (int i = log; i >= 1; i--) {
      if (((l >> i) << i) != l)
        push(l >> i);
      if (((r >> i) << i) != r)
        push((r - 1) >> i);
    }
    int l2 = l, r2 = r;
    while (l < r) {
      if (l & 1)
        all_apply(l++, f);
      if (r & 1)
        all_apply(--r, f);
      l >>= 1, r >>= 1;
    }
    l = l2, r = r2;
    for (int i = 1; i <= log; i++) {
      if (((l >> i) << i) != l)
        update(l >> i);
      if (((r >> i) << i) != r)
        update((r - 1) >> i);
    }
  }
  template <bool (*g)(S)> int max_right(int l) {
    return max_right(l, [](S x) { return g(x); });
  }
  template <class G> int max_right(int l, G g) {
    assert(0 <= l && l <= _n);
    assert(g(e()));
    if (l == _n)
      return _n;
    l += size;
    for (int i = log; i >= 1; i--)
      push(l >> i);
    S sm = e();
    do {
      while (l % 2 == 0)
        l >>= 1;
      if (!g(op(sm, d[l]))) {
        while (l < size) {
          push(l);
          l = (2 * l);
          if (g(op(sm, d[l]))) {
            sm = op(sm, d[l]);
            l++;
          }
        }
        return l - size;
      }
      sm = op(sm, d[l]);
      l++;
    } while ((l & -l) != l);
    return _n;
  }

  template <bool (*g)(S)> int min_left(int r) {
    return min_left(r, [](S x) { return g(x); });
  }
  template <class G> int min_left(int r, G g) {
    assert(0 <= r && r <= _n);
    assert(g(e()));
    if (r == 0)
      return 0;
    r += size;
    for (int i = log; i >= 1; i--)
      push((r - 1) >> i);
    S sm = e();
    do {
      r--;
      while (r > 1 && (r % 2))
        r >>= 1;
      if (!g(op(d[r], sm))) {
        while (r < size) {
          push(r);
          r = (2 * r + 1);
          if (g(op(d[r], sm))) {
            sm = op(d[r], sm);
            r--;
          }
        }
        return r + 1 - size;
      }
      sm = op(d[r], sm);
    } while ((r & -r) != r);
    return 0;
  }

private:
  int _n{}, size{}, log{};
  vector<S> d;
  vector<F> lz;
  void update(int k) { d[k] = op(d[2 * k], d[2 * k + 1]); }
  void all_apply(int k, F f) {
    d[k] = mapping(f, d[k]);
    if (k < size)
      lz[k] = composition(f, lz[k]);
  }
  void push(int k) {
    all_apply(2 * k, lz[k]), all_apply(2 * k + 1, lz[k]);
    lz[k] = id();
  }
  int ceil_pow2(int n) {
    int x = 0;
    while ((1U << x) < (unsigned int)(n))
      x++;
    return x;
  }
};
using node = int;
node op(node l, node r) { return max(l, r); }
node e() { return 0; }
using F = int;
node mapping(F f, node x) { return node{x + f}; }
F composition(F f, F g) { return F{f + g}; } // f(g())
F id() { return F{0}; }
using segtree = lazy_segtree<node, op, e, F, mapping, composition, id>;
auto solve() {
  int n, m, k;
  cin >> n >> m >> k;
  vector<int> l(m), r(m);
  vector<pair<int, int>> op;
  for (int i = 0; i < m; i++) {
    cin >> l[i] >> r[i];
    l[i]--;
    op.emplace_back(l[i], i);
    op.emplace_back(r[i], i);
  }
  ranges::sort(op);
  set<int> cur;
  vector<vector<int>> inter(n);
  vector<int> in(n, 0);
  {
    auto it = op.begin();
    for (int i = 0; i < n; ++i) {
      while (it != op.end() && it->first == i) {
        if (l[it->second] == i) {
          cur.emplace(it->second);
        } else {
          cur.erase(it->second);
        }
        it++;
      }
      in[i] = static_cast<int>(cur.size());
      if (in[i] <= k) {
        for (auto x : cur) {
          inter[i].emplace_back(l[x]);
        }
      }
    }
  }
  dbg(l, r, in, inter);
  vector dp(n, vector<int>(k + 1, 0));
  vector<segtree> seg(k + 1, segtree(n));
  int ans = 0;
  for (int i = 0; i < n; ++i) {
    if (in[i] > k)
      continue;
    inter[i].emplace_back(0);
    inter[i].emplace_back(i);
    sort(inter[i].begin(), inter[i].end());
    dp[i][in[i]] = 1;
    for (auto it = inter[i].begin(); it + 1 < inter[i].end(); it++) {
      if (*it < *next(it)) {
        for (int j = in[i]; j <= k; ++j) {
          dp[i][j] =
              max(dp[i][j], seg[dbg(j - in[i] + (it - inter[i].begin()))].prod(
                                *it, *next(it)) +
                                1);
        }
      }
      for (int j = in[i]; j <= k; ++j) {
        seg[j].set(i, dp[i][j]);
        ans = max(ans, dp[i][j]);
      }
    }
  }
  cout << ans << "\n";
}
auto main() -> int {
  ios::sync_with_stdio(false);
  cin.tie(nullptr), cout.tie(nullptr);
  int _ = 1;
  cin >> _;
  while (_--) {
    solve();
  }
}