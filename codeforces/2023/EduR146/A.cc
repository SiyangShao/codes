using namespace std;
using ll = long long;
auto solve() {
  ll n, k;
  cin >> n >> k;
  ll res = n / k;
  if ((n % k) % 2 == 0 || (k % 2 == 1 && res >= 1)) {
    cout << "YES\n";
  } else {
    cout << "NO\n";
  }
}
auto main() -> int {
  ios::sync_with_stdio(false);
  cin.tie(nullptr), cout.tie(nullptr);
  int _ = 1;
  cin >> _;
  while (_--) {
    solve();
  }
}